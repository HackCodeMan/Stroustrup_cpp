//This program demonstrates how the shift operators("<<" and ">>") and cmath lib work.
// Для понимания этой темы надо знать двоичную систему счисления
#include <iostream> // Работа со стандартными потоками
#include <cmath>  // Работа с математическими операциями (sqrt())
